import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SITE_ORIGIN = 'https://aibizsolution.github.io/blog/';
const SITE_TITLE = 'AI비즈솔루션팀 블로그';
const DEFAULT_DESCRIPTION = '인크로스 AI비즈솔루션팀의 스터디, 업무 적용 사례, AI 도구 활용 자료를 기록하는 정적 블로그입니다.';
const GENERATED_MARKER = '<!-- Generated by scripts/generate-post-pages.mjs. Do not edit directly. -->';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const postsDir = path.join(repoRoot, 'posts');
const templatePath = path.join(repoRoot, 'index.html');
const postsJsonPath = path.join(repoRoot, 'posts.json');

function escapeAttribute(value) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function replaceRequired(html, pattern, replacement, label) {
  if (!pattern.test(html)) {
    throw new Error(`Failed to replace ${label}`);
  }
  const next = html.replace(pattern, replacement);
  return next;
}

function createPostPage(template, post) {
  const title = escapeAttribute(post.title || SITE_TITLE);
  const description = escapeAttribute(post.summary || DEFAULT_DESCRIPTION);
  const tags = escapeAttribute(Array.isArray(post.tags) ? post.tags.join(', ') : '');
  const encodedSlug = encodeURIComponent(post.slug);
  const postUrl = new URL(`posts/${encodedSlug}.html`, SITE_ORIGIN).href;
  const pageTitle = `${title} | ${SITE_TITLE}`;

  let page = template;
  page = replaceRequired(page, /<!DOCTYPE html>/, `<!DOCTYPE html>\n${GENERATED_MARKER}`, 'generated marker');
  page = replaceRequired(page, /<html lang="[^"]*">/, '<html lang="ko">', 'html language');
  page = replaceRequired(page, /(<meta charset="UTF-8">\r?\n)(?!\s*<base\b)/, '$1    <base href="../">\n', 'base href');
  page = replaceRequired(page, /<meta name="description" content="[^"]*">/, `<meta name="description" content="${description}">`, 'meta description');
  page = replaceRequired(page, /<meta property="og:title" content="[^"]*">/, `<meta property="og:title" content="${title}">`, 'og title');
  page = replaceRequired(
    page,
    /<meta property="og:description"\s*\r?\n\s*content="[^"]*">/,
    `<meta property="og:description"\n        content="${description}">`,
    'og description',
  );
  page = replaceRequired(page, /<meta property="og:tags" content="[^"]*">/, `<meta property="og:tags" content="${tags}">`, 'og tags');
  page = replaceRequired(page, /<meta property="og:url" content="[^"]*">/, `<meta property="og:url" content="${postUrl}">`, 'og url');
  page = replaceRequired(page, /<meta name="twitter:title" content="[^"]*">/, `<meta name="twitter:title" content="${title}">`, 'twitter title');
  page = replaceRequired(
    page,
    /<meta name="twitter:description" content="[^"]*">/,
    `<meta name="twitter:description" content="${description}">`,
    'twitter description',
  );
  page = replaceRequired(page, /<link rel="canonical" href="[^"]*">/, `<link rel="canonical" href="${postUrl}">`, 'canonical URL');
  page = replaceRequired(page, /<title>[^<]*<\/title>/, `<title>${pageTitle}</title>`, 'page title');

  return page;
}

const template = await readFile(templatePath, 'utf8');
const posts = JSON.parse(await readFile(postsJsonPath, 'utf8'));

await mkdir(postsDir, { recursive: true });

for (const post of posts) {
  if (!post?.slug) {
    throw new Error('Every post in posts.json must include a slug');
  }

  const outputPath = path.join(postsDir, `${post.slug}.html`);
  await writeFile(outputPath, createPostPage(template, post), 'utf8');
  console.log(`Generated ${path.relative(repoRoot, outputPath)}`);
}
